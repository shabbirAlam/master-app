    let buttonDisabledBackground: Color
    let buttonDisabledText: Color
    
    // MARK: - States
    let success: Color
    let warning: Color
    let error: Color
    let info: Color
    
    // MARK: - Borders / Divider
    let border: Color
    let divider: Color
    
    // MARK: - Overlay
    let overlay: Color
}
